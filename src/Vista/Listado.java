    public void cargarTabla()
    {
        ArrayList<PeliculaDto>lista=con.llenarTabla();
        
        DefaultTableModel modelo=new DefaultTableModel();
        
        String[]encabezado=new String[]{"N°", "Nombre", "Descripcion", "Tipo", "Calificación", "Duración"};
        modelo.setColumnIdentifiers(encabezado);
        
        for (PeliculaDto P : lista) {
            Object[]filas=new Object[]{P.getIdP(), P.getNombreP(), P.getDescripcion(), P.getNombreT(), P.getCalificacion(), P.getDuracion()};
            modelo.addRow(filas);
        }
        tblListado.setModel(modelo);
    }
